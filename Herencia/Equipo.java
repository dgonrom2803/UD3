public class Equipo {
    private String ciudad;
    private String liga;

    public Equipo() {
    }

    public String ciudad () {
        return ciudad;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getLiga() {
        return liga;
    }

    public void setLiga(String liga) {
        this.liga = liga;
    }
}
